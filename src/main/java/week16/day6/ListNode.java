/**
 * @author LuoXuanwei
 * @version 1.0
 * @description 两数相加的节点链表
 * @date 2025/10/2 21:38
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
